from aenum import Enum
class Strenum(Enum):

    @property
    def val(self):
        return self.value[0]

    @property
    def string(self):
        if type(self.value) is tuple and len(self.value) > 1 and type(self.value[1]) is str:
            return self.value[1]
        return self.name
